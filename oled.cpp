// the most basic function, set a single pixel
void Oled::drawPixel(int x, int y, int color) {
  if ((x < 0) || (x >= width()) || (y < 0) || (y >= height()))
    return;

  // check rotation, move pixel around if necessary
  switch (getRotation()) {
  case 1:
    swap(x, y);
    x = WIDTH - x - 1;
    break;
  case 2:
    x = WIDTH - x - 1;
    y = HEIGHT - y - 1;
    break;
  case 3:
    swap(x, y);
    y = HEIGHT - y - 1;
    break;
  }  

  // x is which column
  if (color == WHITE) 
    buffer[x+ (y/8)*LCDWIDTH] |= _BV((y%8));  
  else
    buffer[x+ (y/8)*LCDWIDTH] &= ~_BV((y%8)); 
}
  

void Oled::begin(int vccstate, int i2caddr) {
  _i2caddr = i2caddr;

	wiringPiI2CSetup(i2caddr); // Is this the right place for this?

  // Init sequence for 128x64 OLED module
  command(DISPLAYOFF);                    // 0xAE
  command(SETDISPLAYCLOCKDIV);            // 0xD5
  command(0x80);                                  // the suggested ratio 0x80
  command(SETMULTIPLEX);                  // 0xA8
  command(0x3F);
  command(SETDISPLAYOFFSET);              // 0xD3
  command(0x0);                                   // no offset
  command(SETSTARTLINE | 0x0);            // line #0
  command(CHARGEPUMP);                    // 0x8D
  if (vccstate == EXTERNALVCC) 
    { command(0x10); }
  else 
    { command(0x14); }
  command(MEMORYMODE);                    // 0x20
  command(0x00);                                  // 0x0 act like ks0108
  command(SEGREMAP | 0x1);
  command(COMSCANDEC);
  command(SETCOMPINS);                    // 0xDA
  command(0x12);
  command(SETCONTRAST);                   // 0x81
  if (vccstate == EXTERNALVCC) 
    { command(0x9F); }
  else 
    { command(0xCF); }
  command(SETPRECHARGE);                  // 0xd9
  if (vccstate == EXTERNALVCC) 
    { command(0x22); }
  else 
    { command(0xF1); }
  command(SETVCOMDETECT);                 // 0xDB
  command(0x40);
  command(DISPLAYALLON_RESUME);           // 0xA4
  command(NORMALDISPLAY);                 // 0xA6
  
  command(0xb0);
  command(0x10);
  command(0x01);//Set original position to (0,0)

  command(DISPLAYON);//--turn on oled panel
}


void Oled::invertDisplay(int i) {
  if (i) {
    command(INVERTDISPLAY);
  } else {
    command(NORMALDISPLAY);
  }
}

void Oled::command(int c) { 
  // I2C
  int control = 0x00;   // Co = 0, D/C = 0
  wiringPiI2CWrite(_i2caddr, control);
  wiringPiI2CWrite(_i2caddr, c);
}

